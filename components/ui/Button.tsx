import * as React from 'react';
import { cva, type VariantProps } from 'class-variance-authority';
import { cn } from '../../lib/utils';
import { Loader2 } from 'lucide-react';

const buttonVariants = cva(
  'inline-flex items-center justify-center whitespace-nowrap rounded-button text-sm font-bold ring-offset-background transition-all focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50 active:scale-[0.98]',
  {
    variants: {
      variant: {
        primary: 'bg-berkeley-blue text-california-gold hover:bg-berkeley-dark hover:scale-[1.02] shadow-glass',
        secondary: 'bg-california-gold text-berkeley-blue hover:bg-california-gold/90 hover:scale-[1.02] shadow-md',
        ghost: 'bg-transparent text-berkeley-blue border border-berkeley-blue hover:bg-berkeley-blue/10',
        danger: 'bg-transparent text-error border border-error hover:bg-error/10',
        success: 'bg-verified/10 text-verified border border-verified hover:bg-verified/20',
        glass: 'bg-white/70 backdrop-blur-md text-berkeley-blue border border-white/20 hover:bg-white/90 hover:backdrop-blur-lg shadow-glass',
      },
      size: {
        default: 'h-10 px-4 py-2',
        sm: 'h-9 rounded-md px-3',
        lg: 'h-11 rounded-md px-8',
        icon: 'h-10 w-10',
      },
    },
    defaultVariants: {
      variant: 'primary',
      size: 'default',
    },
  }
);

export interface ButtonProps
  extends React.ButtonHTMLAttributes<HTMLButtonElement>,
    VariantProps<typeof buttonVariants> {
  isLoading?: boolean;
}

const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(
  ({ className, variant, size, isLoading, children, ...props }, ref) => {
    return (
      <button
        className={cn(buttonVariants({ variant, size, className }))}
        ref={ref}
        disabled={isLoading || props.disabled}
        {...props}
      >
        {isLoading && <Loader2 className="mr-2 h-4 w-4 animate-spin" />}
        {children}
      </button>
    );
  }
);
Button.displayName = 'Button';

export { Button, buttonVariants };